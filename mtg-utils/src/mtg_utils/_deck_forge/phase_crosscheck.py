"""Cross-check deck-forge detector firings against phase-rs's parsed classification.

A **read-only audit harness** (like the cube/phase coverage gates — an analysis
tool, never part of the live build path). It surfaces where deck-forge's
hand-written signal detectors and phase-rs's deterministic oracle-text parser
*disagree* about a card, so disagreements can be triaged into detector gaps or
over-fires. It is a noisy second opinion, never ground truth.

phase-rs parses MTGJSON oracle text into a typed effect IR. Its ``card-data.json``
(generated by ``playtest-install-phase`` → ``scripts/gen-card-data.sh``) records,
per face, the parsed ``keywords`` plus ``abilities`` (each carrying an ``effect``
whose ``type`` is an Effect discriminant), ``triggers`` (each a ``mode``
TriggerMode), ``static_abilities`` (each a ``mode`` StaticMode), and
``replacements``. We project each face to a normalized tag set and diff it against
the signal keys ``extract_signals`` fires on the same card, joined by Scryfall
``oracle_id`` (present on both sides).

Two caveats shape the design:

1. phase's parse is deterministic but **incomplete** — a face whose
   ``parse_warnings`` is non-empty punted on some text, so phase's silence there
   is a parse gap, not a real disagreement (segregated via ``include_low_confidence``).

2. phase's IR describes what a card *does*; deck-forge's ``*_matters`` lanes
   describe synergy **payoffs** ("whenever X happens, …"). So the ``CROSSWALK``
   maps *payoff* lanes to phase **triggers** (the "whenever" shape) and *doer*
   lanes (token_maker, direct_damage, untap_engine) to phase **effects**. Mixing
   the two floods PHASE_ONLY with "card does X but isn't a payoff for X" noise.

Three verdicts per (card, lane):

  * ``BOTH``          — detector fires & phase corroborates  → agreement
  * ``DETECTOR_ONLY`` — detector fires, phase silent          → over-fire OR phase gap
  * ``PHASE_ONLY``    — phase corroborates, detector silent   → candidate detector gap
"""

