BOTH = "both"
DETECTOR_ONLY = "detector_only"
PHASE_ONLY = "phase_only"


def detector_lanes(card: dict) -> frozenset[str]:
    """The set of signal keys deck-forge's detectors fire on ``card``."""
    return frozenset(s.key for s in extract_signals_hybrid(card, ir_for(card)))


def classify_card(
    fired: Iterable[str],
    tags: Iterable[str],
    crosswalk: dict[str, frozenset[str]] = CROSSWALK,
) -> dict[str, str]:
    """Verdict per crosswalk lane that either side touches (others omitted)."""
    fired_set = set(fired)
    tag_set = set(tags)
    out: dict[str, str] = {}
    for lane, phase_corroborators in crosswalk.items():
        fires = lane in fired_set
        corroborated = bool(tag_set & phase_corroborators)
        if fires and corroborated:
            out[lane] = BOTH
        elif fires:
            out[lane] = DETECTOR_ONLY
        elif corroborated:
            out[lane] = PHASE_ONLY
    return out


def crosscheck_corpus(
    cards: Iterable[dict],
    phase_index: dict[str, dict],
    *,
    crosswalk: dict[str, frozenset[str]] = CROSSWALK,
    include_low_confidence: bool = True,
    example_cap: int = 8,
    extract: Callable[[dict], Iterable[str]] = detector_lanes,
) -> dict:
    """Join ``cards`` to ``phase_index`` by ``oracle_id`` and tally agreement.

    ``phase_index`` maps ``oracle_id`` → phase face record (see ``load_phase_index``).
    Returns a structured report: per-lane both/detector_only/phase_only counts with
    capped example card names, join/skip stats, the detector lanes that fired but
    aren't in the crosswalk (cross-check blind spots), and the phase tags that
    corroborate no lane (candidates to grow the crosswalk).
    """
    mapped_tags: set[str] = set().union(*crosswalk.values()) if crosswalk else set()
    lanes: dict[str, dict] = {}
    unmapped_detector: dict[str, int] = {}
    unmapped_phase: dict[str, int] = {}
    joined = 0
    skipped = {"no_oracle_id": 0, "not_in_phase": 0, "low_confidence": 0}

    for card in cards:
        oracle_id = card.get("oracle_id")
        if not oracle_id:
            skipped["no_oracle_id"] += 1
            continue
        record = phase_index.get(oracle_id)
        if record is None:
            skipped["not_in_phase"] += 1
            continue
        if not include_low_confidence and record.get("parse_warnings"):
            skipped["low_confidence"] += 1
            continue

        joined += 1
        fired = set(extract(card))
        tags = phase_tags(record)
        name = card.get("name", "")

        for lane, verdict in classify_card(fired, tags, crosswalk).items():
            tally = lanes.setdefault(
                lane, {BOTH: 0, DETECTOR_ONLY: 0, PHASE_ONLY: 0, "examples": {}}
            )
            tally[verdict] += 1
            examples = tally["examples"].setdefault(verdict, [])
            if len(examples) < example_cap and name not in examples:
                examples.append(name)

        for lane in fired:
            if lane not in crosswalk:
                unmapped_detector[lane] = unmapped_detector.get(lane, 0) + 1
        for tag in tags:
            if tag not in mapped_tags:
                unmapped_phase[tag] = unmapped_phase.get(tag, 0) + 1

    return {
        "lanes": lanes,
        "joined": joined,
        "skipped": skipped,
        "unmapped_detector_lanes": unmapped_detector,
        "unmapped_phase_tags": unmapped_phase,
    }


def load_phase_index(path: str | Path | None = None) -> dict[str, dict]:
    """Build an ``oracle_id`` → face-record index from phase's ``card-data.json``.

    Defaults to the path our ``playtest-install-phase`` writes. ``card-data.json``
    is keyed by lowercased face name; we re-key by ``scryfall_oracle_id`` (first
    face wins, so multi-face cards resolve to their front face).
    """
    if path is None:
        from mtg_utils import _phase

        path = _phase._card_data_path()  # noqa: SLF001 — canonical phase card-data path
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(
            f"phase card-data.json not found at {path}. "
            "Run `playtest-install-phase` to generate it."
        )
    data = json.loads(path.read_text())
    records = data.values() if isinstance(data, dict) else data
    index: dict[str, dict] = {}
    for record in records:
        oracle_id = record.get("scryfall_oracle_id")
        if oracle_id:
            index.setdefault(oracle_id, record)
    return index


def load_cards(path: str | Path) -> list[dict]:
    """Load a card corpus from JSON: a plain list of card dicts, or a parsed-deck
    dict (``commanders`` / ``cards`` / ``sideboard``) which is flattened."""
    data = json.loads(Path(path).read_text())
    if isinstance(data, list):
        return data
    cards: list[dict] = []
    for section in ("commanders", "cards", "sideboard"):
        cards.extend(data.get(section) or [])
    return cards


def render_report(report: dict) -> str:
    """Render a cross-check report as a markdown digest, lanes ordered by the
    actionable signal (most disagreement first)."""
    lines: list[str] = ["# phase-rs ↔ deck-forge detector cross-check", ""]
    skipped = report["skipped"]
    lines.append(
        f"Joined **{report['joined']}** cards by oracle_id "
        f"(skipped: {skipped['no_oracle_id']} no-oracle_id, "
        f"{skipped['not_in_phase']} not-in-phase, "
        f"{skipped['low_confidence']} low-confidence parse).",
    )
    lines.append("")

    lanes = report["lanes"]
    if lanes:
        lines += [
            "## Lanes (by disagreement)",
            "",
            "| lane | both | detector-only | phase-only |",
            "| --- | ---: | ---: | ---: |",
        ]
        ordered = sorted(
            lanes.items(),
            key=lambda kv: (kv[1][DETECTOR_ONLY] + kv[1][PHASE_ONLY], kv[1][BOTH]),
            reverse=True,
        )
        for lane, tally in ordered:
            both, det, phase = tally[BOTH], tally[DETECTOR_ONLY], tally[PHASE_ONLY]
            lines.append(f"| {lane} | {both} | {det} | {phase} |")
        lines.append("")
        # Per-lane examples for the disagreement buckets (the triage worklist).
        for lane, tally in ordered:
            for bucket in (PHASE_ONLY, DETECTOR_ONLY):
                names = tally["examples"].get(bucket) or []
                if names:
                    lines.append(f"- **{lane}** {bucket}: {', '.join(names)}")
        lines.append("")

    unmapped_det = report["unmapped_detector_lanes"]
    if unmapped_det:
        lines.append("## Detector lanes with no crosswalk mapping (blind spots)")
        lines.append("")
        for lane, count in sorted(unmapped_det.items(), key=lambda kv: -kv[1]):
            lines.append(f"- {lane}: {count}")
        lines.append("")

    unmapped_phase = report["unmapped_phase_tags"]
    if unmapped_phase:
        lines.append("## Top phase tags mapping to no lane (grow the crosswalk)")
        lines.append("")
        for tag, count in sorted(unmapped_phase.items(), key=lambda kv: -kv[1])[:25]:
            lines.append(f"- {tag}: {count}")
        lines.append("")

    return "\n".join(lines)


