def _norm(token: str) -> str:
    """Lowercase + strip non-alphanumerics, so ``DealDamage``/``deal_damage`` match."""
    return re.sub(r"[^a-z0-9]", "", token.lower())


_EFFECT_VOCAB = frozenset(_norm(t) for t in _EFFECT_KINDS)
_TRIGGER_VOCAB = frozenset(_norm(t) for t in _TRIGGER_MODES)
_STATIC_VOCAB = frozenset(_norm(t) for t in _STATIC_MODES)

_PHASE_ABILITY_ARRAYS = ("abilities", "triggers", "static_abilities", "replacements")


def _collect_tokens(node: object, out: set[str]) -> None:
    """Collect candidate enum discriminants from a parsed-ability subtree.

    Two sources, chosen to dodge struct field-name collisions (e.g. a ``"mana"``
    cost key vs. the ``Mana`` EffectKind):

      * every string leaf — captures ``tag="type"`` discriminants (the value of
        ``type``/``mode``/``kind``) and unit-variant strings; and
      * the single key of an externally-tagged enum dict — a one-key dict whose
        key is PascalCase, e.g. ``{"ReduceCost": {...}}``.

    Multi-key struct keys are never collected, so snake_case field names can't
    masquerade as mechanic tags.
    """
    if isinstance(node, str):
        out.add(node)
    elif isinstance(node, dict):
        if len(node) == 1:
            ((key, value),) = node.items()
            if isinstance(key, str) and key[:1].isupper():
                out.add(key)
            _collect_tokens(value, out)
        else:
            for value in node.values():
                _collect_tokens(value, out)
    elif isinstance(node, list):
        for item in node:
            _collect_tokens(item, out)


def phase_tags(record: dict) -> frozenset[str]:
    """Project a phase ``card-data.json`` face record to namespaced mechanic tags.

    Returns a set of ``effect:<x>`` / ``trigger:<x>`` / ``static:<x>`` / ``kw:<x>``
    tags (one token may yield several namespaces, e.g. ``TapAll`` is both an
    effect and a trigger). Tokens outside the baked phase vocabularies are dropped
    as structural noise.
    """
    raw: set[str] = set()
    for array in _PHASE_ABILITY_ARRAYS:
        _collect_tokens(record.get(array) or [], raw)

    tags: set[str] = set()
    for token in raw:
        norm = _norm(token)
        if norm in _EFFECT_VOCAB:
            tags.add(f"effect:{norm}")
        if norm in _TRIGGER_VOCAB:
            tags.add(f"trigger:{norm}")
        if norm in _STATIC_VOCAB:
            tags.add(f"static:{norm}")

    for keyword in record.get("keywords") or []:
        if isinstance(keyword, str):
            tags.add(f"kw:{_norm(keyword)}")
        elif isinstance(keyword, dict) and keyword:
            first_key = next(iter(keyword))
            if isinstance(first_key, str):
                tags.add(f"kw:{_norm(first_key)}")
    return frozenset(tags)


