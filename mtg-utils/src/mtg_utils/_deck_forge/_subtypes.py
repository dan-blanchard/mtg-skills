"""Creature-subtype vocabulary + subject-resolution constants for signal extraction.

CREATURE_SUBTYPES is self-derived from Scryfall bulk type_lines (creature faces only,
split on // first so adventure/split spell faces never leak "sorcery"/"instant").
It is the allowlist that validates a captured tribal subject so junk captures (a clone
clause, a flavor noun) resolve to nothing rather than a false-positive kindred signal.

Regenerate after large set releases (the one-off: load bulk -> for creature faces,
split_type_line -> subtypes, lowercased, len>=3, alpha, minus NON_SUBJECT_WORDS).
New creature types are rare, so drift is slow. NOTE: the len>=3 filter drops the only
real two-letter creature subtype, "ox" — re-add it by hand after any regen.
"""

