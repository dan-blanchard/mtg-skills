# GATE 3 — injected drift fails loud
# ---------------------------------------------------------------------------


def test_injected_drift_unknown_field():
    schema = _schema()
    name, rec = next(iter(_samples().items()))
    drifted = copy.deepcopy(rec)
    node = _first_tagged(drifted)
    assert node is not None, "sample has no tagged node to mutate"
    node[0]["__phantom_field__"] = 1
    with pytest.raises(MirrorDriftError, match="unknown field"):
        strict_load_card(drifted, schema, name=name)


def test_injected_drift_renamed_tag():
    schema = _schema()
    name, rec = next(iter(_samples().items()))
    drifted = copy.deepcopy(rec)
    node = _first_tagged(drifted)
    assert node is not None
    node[0]["type"] = "__RenamedNonexistentVariant__"
    with pytest.raises(MirrorDriftError, match="unknown tagged node"):
        strict_load_card(drifted, schema, name=name)


def test_injected_drift_zero_instance_variant_closed_union():
    """Relocating a real effect-slot node onto a zero-instance Effect variant
    fails with the closed-union message on first emission."""
    schema = _schema()
    target = None
    for name, rec in _samples().items():
        if _find_tagged_at(rec, "effect"):
            target = (name, rec)
            break
    if target is None:
        pytest.skip("no effect-slot tagged node in samples")
    name, rec = target
    drifted = copy.deepcopy(rec)
    node = _find_tagged_at(drifted, "effect")
    node.clear()
    node["type"] = "Cascade"
    with pytest.raises(MirrorDriftError, match="zero-instance phase Effect"):
        strict_load_card(drifted, schema, name=name)


def test_injected_drift_missing_required_field():
    schema = _schema()
    target_name = None
    for name, rec in _samples().items():
        if _find_required(rec, schema):
            target_name = name
            break
    if target_name is None:
        pytest.skip("no required-field-bearing tagged node in samples")
    drifted = copy.deepcopy(_samples()[target_name])
    node, _ckey, _tag, required_field = _find_required(drifted, schema)
    node.pop(required_field)
    with pytest.raises(MirrorDriftError, match="missing required field"):
        strict_load_card(drifted, schema, name=target_name)


# ---------------------------------------------------------------------------
# GATE 4 — losslessness round-trip
# ---------------------------------------------------------------------------


def test_losslessness_roundtrip_samples():
    schema = _schema()
    for name, rec in _samples().items():
        loaded = strict_load_card(rec, schema, name=name, build=True)
        assert loaded is not None
        assert loaded.to_dict() == rec, f"{name} dropped a field on round-trip"


def test_losslessness_roundtrip_full_corpus():
    records = _card_data_records()
    schema = infer_schema(records, phase_tag=_phase.PHASE_TAG)
    # Round-trip a deterministic spread (every 1000th card) for speed.
    checked = 0
    for rec in records[::1000]:
        loaded = strict_load_card(rec, schema, build=True)
        assert loaded.to_dict() == rec
        checked += 1
    assert checked >= 30


