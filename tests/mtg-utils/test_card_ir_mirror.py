"""Stage-1 gate for the lossless phase-mirror substrate (ADR-0035).

The substrate is ADDITIVE — nothing in production reads it. These tests are the
shipped deliverable: the loud-on-bump drift detector plus the data-inference
codegen's correctness gates.

Two tiers:

* **Committed-fixture tests** (always run, CI-safe): exercise the generated
  mirror schema + a diverse real-card slice + the per-variant baseline, all
  committed under ``tests/fixtures/`` (regenerated by ``build-card-ir-substrate``).
* **Full-corpus tests** (skip unless the pinned 93MB ``card-data.json`` is
  present locally): re-infer + strict-load all 35,396 cards. These are the
  gated dev gate, never CI.
"""

