def test_effect_roster_shape():
    assert len(EFFECT_VARIANTS) == 207
    assert len(ZERO_INSTANCE_EFFECTS) == 18
    assert set(EFFECT_VARIANTS) >= ZERO_INSTANCE_EFFECTS
    # the ADR's four named examples are in the closed-union arm
    for name in ("Cascade", "Exploit", "MiracleCast", "VentureInto"):
        assert name in ZERO_INSTANCE_EFFECTS


# ---------------------------------------------------------------------------
# GATE 1 — discriminator uniqueness
# ---------------------------------------------------------------------------


def test_discriminator_uniqueness_committed_schema():
    """Every (content_key, tag) group resolves to ONE payload schema — no field
    mixes ``tagged`` with another container kind (undecidable dispatch)."""
    schema = _schema()
    ambiguous = find_ambiguous_fields(schema)
    assert ambiguous == [], ambiguous


def test_discriminator_uniqueness_full_corpus():
    records = _card_data_records()
    schema = infer_schema(records, phase_tag=_phase.PHASE_TAG)
    ambiguous = find_ambiguous_fields(schema)
    assert ambiguous == [], ambiguous
    # the inference is a function: each (ckey, tag) -> exactly one GroupSchema
    assert len({(g.ckey, g.tag) for g in schema.tagged.values()}) == len(schema.tagged)


# ---------------------------------------------------------------------------
# GATE 2 — strict-load passes over the full corpus
# ---------------------------------------------------------------------------


def test_strict_load_full_corpus():
    records = _card_data_records()
    schema = infer_schema(records, phase_tag=_phase.PHASE_TAG)
    loaded = 0
    for rec in records:
        strict_load_card(rec, schema, build=False)
        loaded += 1
    assert loaded == len(records)
    assert loaded >= 35000


def test_strict_load_accepts_committed_samples():
    schema = _schema()
    for name, rec in _samples().items():
        strict_load_card(rec, schema, name=name, build=False)


